/* Round constants (words, MSByte first) */
static const uint32_t Rcon[10] = {
    0x01000000,0x02000000,0x04000000,0x08000000,0x10000000,0x20000000,0x40000000,0x80000000,0x1B000000,0x36000000
};

/* --- helper macros ------------------------------------------------------ */
#define GETU32(p) (((uint32_t)(p)[0] << 24) ^ ((uint32_t)(p)[1] << 16) ^ ((uint32_t)(p)[2] <<  8) ^ ((uint32_t)(p)[3]))
#define PUTU32(p,v)  do { (p)[0]=(uint8_t)((v)>>24); (p)[1]=(uint8_t)((v)>>16);  (p)[2]=(uint8_t)((v)>>8);  (p)[3]=(uint8_t)(v); } while(0)

static inline uint32_t RotWord(uint32_t w) { return (w << 8) | (w >> 24); }
static inline uint32_t SubWord(uint32_t w) {
    return ((uint32_t)sbox[w >> 24] << 24) |
           ((uint32_t)sbox[(w >> 16) & 0xff] << 16) |
           ((uint32_t)sbox[(w >> 8)  & 0xff] << 8 ) |
           ((uint32_t)sbox[w & 0xff]);
}

/* ----------------------------------------------------------------------- */
void aes_key_setup(aes_key_t *ks, const uint8_t *key, size_t key_bits)
{
    int Nk = (int)(key_bits / 32);          /* words in the input key */
    int Nb = 4;                             /* AES block is always 4 words */
    ks->Nr = Nk + 6;                        /* 10/12/14 */

    int i = 0;
    for (; i < Nk; ++i)
        ks->rk[i] = GETU32(key + 4*i);

    for (; i < Nb * (ks->Nr + 1); ++i) {
        uint32_t temp = ks->rk[i - 1];
        if (i % Nk == 0)
            temp = SubWord(RotWord(temp)) ^ Rcon[(i / Nk) - 1];
        else if (Nk > 6 && i % Nk == 4)
            temp = SubWord(temp);
        ks->rk[i] = ks->rk[i - Nk] ^ temp;
    }
}

/* Multiply by 2 in GF(2^8) */
static inline uint8_t xtime(uint8_t x) { return (x << 1) ^ ((x >> 7) * 0x1b); }

static void SubBytes(uint8_t s[16]) {
    for (int i = 0; i < 16; ++i) s[i] = sbox[s[i]];
}
static void InvSubBytes(uint8_t s[16]) {
    for (int i = 0; i < 16; ++i) s[i] = rsbox[s[i]];
}

static void ShiftRows(uint8_t s[16]) {
    uint8_t t;
    /* row 1 left shift by 1 */
    t = s[1];  s[1]  = s[5];  s[5]  = s[9];  s[9]  = s[13]; s[13] = t;
    /* row 2 left shift by 2 */
    t = s[2];  s[2]  = s[10]; s[10] = t;
    t = s[6];  s[6]  = s[14]; s[14] = t;
    /* row 3 left shift by 3 */
    t = s[3];  s[3]  = s[15]; s[15] = s[11]; s[11] = s[7];  s[7]  = t;
}
static void InvShiftRows(uint8_t s[16]) {
    uint8_t t;
    /* row 1 right shift by 1 */
    t = s[13]; s[13] = s[9];  s[9]  = s[5];  s[5]  = s[1];  s[1]  = t;
    /* row 2 right shift by 2 */
    t = s[2];  s[2]  = s[10]; s[10] = t;
    t = s[6];  s[6]  = s[14]; s[14] = t;
    /* row 3 right shift by 3 */
    t = s[3];  s[3]  = s[7];  s[7]  = s[11]; s[11] = s[15]; s[15] = t;
}

static void MixColumns(uint8_t s[16]) {
    for (int c = 0; c < 4; ++c) {
        uint8_t *col = &s[4*c];
        uint8_t a0 = col[0], a1 = col[1], a2 = col[2], a3 = col[3];
        uint8_t t  = a0 ^ a1 ^ a2 ^ a3;
        uint8_t xt0 = xtime(a0 ^ a1); col[0] ^= t ^ xt0;
        uint8_t xt1 = xtime(a1 ^ a2); col[1] ^= t ^ xt1;
        uint8_t xt2 = xtime(a2 ^ a3); col[2] ^= t ^ xt2;
        uint8_t xt3 = xtime(a3 ^ a0); col[3] ^= t ^ xt3;
    }
}
static void InvMixColumns(uint8_t s[16]) {
    for (int c = 0; c < 4; ++c) {
        uint8_t *col = &s[4*c];
        uint8_t a0 = col[0], a1 = col[1], a2 = col[2], a3 = col[3];
        uint8_t x0 = xtime(xtime(a0 ^ a2));
        uint8_t x1 = xtime(xtime(a1 ^ a3));
        col[0] ^= x0; col[1] ^= x1; col[2] ^= x0; col[3] ^= x1;
    }
    MixColumns(s); /* one more MixColumns yields InvMixColumns */
}

static void AddRoundKey(uint8_t s[16], const uint32_t *rk) {
    for (int c = 0; c < 4; ++c) {
        uint32_t k = rk[c];
        s[4*c+0] ^= (uint8_t)(k >> 24);
        s[4*c+1] ^= (uint8_t)(k >> 16);
        s[4*c+2] ^= (uint8_t)(k >> 8 );
        s[4*c+3] ^= (uint8_t)(k      );
    }
}

void aes_encrypt_block(const aes_key_t *ks, const uint8_t in[16], uint8_t out[16])
{
    uint8_t state[16];
    memcpy(state, in, 16);
    AddRoundKey(state, ks->rk);

    int round = 1;
    for (; round < ks->Nr; ++round) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, ks->rk + 4*round);
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, ks->rk + 4*round);

    memcpy(out, state, 16);
}

void aes_decrypt_block(const aes_key_t *ks, const uint8_t in[16], uint8_t out[16])
{
    uint8_t state[16];
    memcpy(state, in, 16);

    AddRoundKey(state, ks->rk + 4*ks->Nr);
    InvShiftRows(state);
    InvSubBytes(state);

    int round = ks->Nr - 1;
    for (; round > 0; --round) {
        AddRoundKey(state, ks->rk + 4*round);
        InvMixColumns(state);
        InvShiftRows(state);
        InvSubBytes(state);
    }

    AddRoundKey(state, ks->rk);
    memcpy(out, state, 16);
}
